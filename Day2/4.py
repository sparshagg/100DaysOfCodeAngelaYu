# Mathematical Operators in Python

# Addition
print(3+5)

# Subtraction
print(5-3)

# Multiplication
print(3*5)

# Division - This always gives us a float value.
a = (6/3)
print(type(a))

# Power
print(4**2)

# Floor Division
print(4//2)

# Order is PEMDAS
# () > ** > * > / > + > -    Paranthesis > Exponents > Multiplication > Addition > Subtraction

print(3 * 3 + 3 / 3 - 3) # Order will be PEMDAS 
# Answer will be 7
print((3 * 3 + 3 / 3 - 3) - 4)
# Answer will be 3